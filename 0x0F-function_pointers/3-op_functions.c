#include "3-calc.h"

/**
 * op_add - f
 * @a: 1
 * @b: 2
 * Return: r
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - f
 * @a: 1
 * @b: 2
 * Return: r
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - f
 * @a: 1
 * @b: 2
 * Return: r
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - f
 * @a: 1
 * @b: 2
 * Return: r
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - f
 * @a: 1
 * @b: 2
 * Return: r
 */
int op_mod(int a, int b)
{
	return (a % b);
}
